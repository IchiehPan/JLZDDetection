package com.example.demo.entity;

public class AlarmPoint {

	private String x;
	private String y;
		
	
	public AlarmPoint(String x, String y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	public AlarmPoint() {
		super();
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}

	
	
	
}
